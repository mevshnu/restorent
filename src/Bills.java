public class Bills
{
    String name;
    long mobile;
    int tea;
    int coffee;
    int snacks;
    int idili;
    int dosha;
    int sum;

    public Bills(String name, long mobile, int tea, int coffee, int snacks, int idili, int dosha, int sum)
    {
        this.name = name;
        this.mobile = mobile;
        this.tea = tea;
        this.coffee = coffee;
        this.snacks = snacks;
        this.idili = idili;
        this.dosha = dosha;
        this.sum = sum;
    }
}

